package com.shadow.shadow

class MessageFromFacebook {
    private String mid
    private String text

    MessageFromFacebook() {
        this.mid = mid
        this.text = text
    }

    String getMid() {
        return mid
    }

    String getText() {
        return text
    }

    void setMid(String mid) {
        this.mid = mid
    }

    void setText(String text) {
        this.text = text
    }
}
